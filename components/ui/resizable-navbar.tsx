export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-4 py-2 rounded-md bg-white button bg-white text-black text-sm font-bold relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-block text-center";

  const variantStyles = {
    primary:
      "shadow-[0_0_24px_rgba(34,_42,_53,_0.06),_0_1px_1px_rgba(0,_0,_0,_0.05),_0_0_0_1px_rgba(34,_42,_53,_0.04),_0_0_4px_rgba(34,_42,_53,_0.08),_0_16px_68px_rgba(47,_48,_55,_0.05),_0_1px_0_rgba(255,_255,_255,_0.1)_inset]",
    secondary: "bg-transparent shadow-none dark:text-white",
    dark: "bg-black text-white shadow-[0_0_24px_rgba(34,_42,_53,_0.06),_0_1px_1px_rgba(0,_0,_0,_0.05),_0_0_0_1px_rgba(34,_42,_53,_0.04),_0_0_4px_rgba(34,_42,_53,_0.08),_0_16px_68px_rgba(47,_48,_55,_0.05),_0_1px_0_rgba(255,_255,_255,_0.1)_inset]",
    gradient:
      "bg-gradient-to-b from-blue-500 to-blue-700 text-white shadow-[0px_2px_0px_0px_rgba(255,255,255,0.3)_inset]",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
export const MobileNavItems = ({
  items,
  onItemClick,
}: {
  items: {
    name: string;
    link: string;
    children?: {
      name: string;
      link: string;
    }[];
  }[];
  onItemClick?: () => void;
}) => {
  const [openIndex, setOpenIndex] = useState<number | null>(null);

  const toggleSubmenu = (index: number) => {
    setOpenIndex(openIndex === index ? null : index);
  };

  return (
    <div className="flex w-full flex-col gap-2">
      {items.map((item, idx) => (
        <div key={`mobile-nav-${idx}`} className="w-full">
          <div
            className="flex items-center justify-between w-full py-2 text-base font-medium text-black dark:text-white"
            onClick={() => {
              if (item.children) toggleSubmenu(idx);
              else onItemClick?.();
            }}
          >
            <a href={item.link} className="flex-1" onClick={onItemClick}>
              {item.name}
            </a>
            {item.children && (
              <button onClick={() => toggleSubmenu(idx)}>
                {openIndex === idx ? (
                  <IconChevronUp size={20} />
                ) : (
                  <IconChevronDown size={20} />
                )}
              </button>
            )}
          </div>

          {/* Submenu */}
          {item.children && openIndex === idx && (
            <div className="ml-4 mt-1 flex flex-col gap-1">
              {item.children.map((child, childIdx) => (
                <a
                  key={`mobile-sub-${childIdx}`}
                  href={child.link}
                  onClick={onItemClick}
                  className="py-1 text-sm text-gray-600 dark:text-gray-300 hover:underline"
                >
                  {child.name}
                </a>
              ))}
            </div>
          )}
        </div>
      ))}
    </div>
  );
};